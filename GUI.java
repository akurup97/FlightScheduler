    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (add == 1){
            Customer.addCustomer(addTextField1.getText());
            javax.swing.JOptionPane.showMessageDialog(this, "New customer added: " + addTextField1.getText());
        }
        else if (add == 2){
            Flight.addFlight(addTextField1.getText(), Integer.parseInt(addTextField2.getText()));
            javax.swing.JOptionPane.showMessageDialog(this, "New flight added: " + addTextField1.getText() + " with " + addTextField2.getText() + " seats.");
        }
        else if (add == 3){
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            try {
                Day.addDay(new java.sql.Date(sdf.parse(addTextField1.getText()).getTime()));
                javax.swing.JOptionPane.showMessageDialog(this, "New day added: " + addTextField1.getText());
            }
            catch(Exception e){
                javax.swing.JOptionPane.showMessageDialog(this, "Please use the YYYY-MM-DD date format");
            }
        }
        else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please specify what to add.");
        }
        refresh();
    }//GEN-LAST:event_addButtonActionPerformed

