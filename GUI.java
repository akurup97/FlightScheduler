    private void statusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_statusButtonActionPerformed
                // TODO add your handling code here:
        String statusResult="";
        if (status == 1){
            statusResult = "Date: " + statusComboBox1.getSelectedItem().toString()+"\nFlight: " + statusComboBox2.getSelectedItem().toString()+"\n";
            statusResult += Status.statusFlight(statusComboBox1.getSelectedItem().toString(),statusComboBox2.getSelectedItem().toString());
        }
        else if (status == 2){
            statusResult = "Waitlist for " + statusComboBox1.getSelectedItem().toString() + "\n";
            statusResult += Status.statusWaitlist(statusComboBox1.getSelectedItem().toString());
        }
        else if (status == 3){
            statusResult ="Customer: "+statusComboBox1.getSelectedItem().toString()+"\n";
            statusResult += Status.statusCustomer(statusComboBox1.getSelectedItem().toString());
        }
        else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a status.");
        }
        jTextArea1.setText(statusResult);
    }//GEN-LAST:event_statusButtonActionPerformed

